    public void cambiarPanel(javax.swing.JPanel nuevoPanel) {
        panPrincipal.removeAll();
        panPrincipal.setLayout(new BorderLayout());
        panPrincipal.add(nuevoPanel, BorderLayout.CENTER);
        panPrincipal.revalidate();
        panPrincipal.repaint();
    }

    public void mostrarPanelInicio() {
        if (this.frm == null) {
            this.frm = new JPInicio(this, this.silabo);
        }
        this.frm.setDatosSilabo(this.silabo);

        btnInicio.setBackground(COLOR_NO_SELECCIONADO);
        btnGenerador.setBackground(COLOR_SELECCIONADO);
        btnPerfil.setBackground(COLOR_NO_SELECCIONADO);

        cambiarPanel(this.frm);
    }

