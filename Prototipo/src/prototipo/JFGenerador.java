    private void btnGeneradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneradorActionPerformed
        if (!bandera) {
            if (this.principal == null || !this.principal.isDisplayable()) {
                this.principal = new JFPrincipal(this, this.silabo);
            }
            this.principal.setVisible(true);
            this.principal.setLocationRelativeTo(null);

        } else {
            this.mostrarPanelInicio();
        }
    }//GEN-LAST:event_btnGeneradorActionPerformed

