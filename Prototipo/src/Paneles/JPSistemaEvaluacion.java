    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int pos = this.tblSistemaEvaluacion.getSelectedRow();

        if (pos > -1) {
            String descripcionDesempeño = modeloEvalu.getDesempeño(pos).getDescripcion();

            // Buscar evidencias de la unidad que tenga ese desempeño
            List<Evidencia> evidenciasDelDesempeño = new ArrayList<>();
            for (Unidad unidad : unidades) {
                if (unidad.getDesempeño().equals(descripcionDesempeño)) {
                    evidenciasDelDesempeño = unidad.getEvidencias();
                    break;
                }
            }

            // Pasar las evidencias al JDialog
            JDSistemaEvaluacion frm = new JDSistemaEvaluacion(null, true, evidenciasDelDesempeño);
            frm.setDesempeño(descripcionDesempeño);
            frm.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una unidad");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

