    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int pos = this.tblSistemaEvaluacion.getSelectedRow();

        if (pos > -1) {
            String descripcionDesempeño = modeloEvalu.getDesempeño(pos).getDescripcion();

            // Buscar evidencias que ya tienen instrumentos asociados (en la unidad correspondiente)
            List<Evidencia> evidenciasDelDesempeño = new ArrayList<>();
            for (Unidad unidad : unidades) {
                if (unidad.getDesempeño().equals(descripcionDesempeño)) {
                    evidenciasDelDesempeño = unidad.getEvidencias();  // traer todas las evidencias (con instrumentos ya asignados)
                    break;
                }
            }

            if (evidenciasDelDesempeño.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay evidencias para este desempeño aún.");
                return;
            }

            // Abrir el formulario para editar evidencias e instrumentos
            JDSistemaEvaluacion frm = new JDSistemaEvaluacion(null, true, evidenciasDelDesempeño);
            frm.setDesempeño(descripcionDesempeño);
            frm.setVisible(true);

            // Al cerrar el JDialog, si se aceptó, actualizar las evidencias en la unidad correspondiente
            if (frm.wasAccepted()) {
                List<Evidencia> evidenciasActualizadas = frm.getEvidenciasActualizadas();

                // Volver a guardar las evidencias en la unidad correspondiente
                for (Unidad unidad : unidades) {
                    if (unidad.getDesempeño().equals(descripcionDesempeño)) {
                        unidad.setEvidencias(evidenciasActualizadas);
                        break;
                    }
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un desempeño.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed


