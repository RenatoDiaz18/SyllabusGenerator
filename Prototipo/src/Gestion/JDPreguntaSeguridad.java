    void setEnunciadoPregunta(String intentoCorreo) {
        for(Docente doc : docentes){
            if(doc.getCorreo().equals(intentoCorreo)){
                docenteRecuperando = doc;
                this.lblEnunciadoPregunta.setText(PREGUNTAS_SEGURIDAD[doc.getIndexPreguntaSeguridad()]);
                respuestaValida = doc.getRespuestaSeguridad();
                break;
            }
        }
    }

    private boolean validarDatos() {
        if(this.txtRespuesta.getText().isBlank() || this.txtRespuesta.getText().isEmpty() || this.txtRespuesta.getText().equals("Ingrese su respuesta")){
            this.lblRespuestaIncorrecta.setText("Respuesta requerida.");
            lblRespuestaIncorrecta.setVisible(true);
            
            this.txtRespuesta.setForeground(Color.LIGHT_GRAY);
            this.txtRespuesta.setText("Ingrese su respuesta");
            
            return false;
        }else{
            return true;
        }
    }
