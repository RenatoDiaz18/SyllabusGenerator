    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        if(validarDatos()){
            if(respuestaValida.equalsIgnoreCase(this.txtRespuesta.getText())){
                JDCambiarContraseña frmCambiarContraseña = new JDCambiarContraseña(null, true);
                frmCambiarContraseña.setLocationRelativeTo(this);
                this.setVisible(false);
                frmCambiarContraseña.cambiarContraseña(docenteRecuperando);
            }else{
                numIntentos++;
                if(numIntentos == 3){
                    this.dispose();
                    MostrarMensaje.alert("Ha superado el número de intentos.\nVuelva a iniciar el programa e intente de nuevo");
                    System.exit(0);
                }else{
                    this.lblRespuestaIncorrecta.setText("Respuesta incorrecta (Intentos restantes: "+ (3-numIntentos) + ")");
                    this.lblRespuestaIncorrecta.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btnValidarActionPerformed

