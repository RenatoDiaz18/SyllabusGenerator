    private boolean ValidarDatos() {
        String errorMessage = "";
        
        if(this.txtCorreo.getText().isBlank() || this.txtCorreo.getText().isEmpty() || this.txtCorreo.getText().equals("example@unprg.edu.pe")){
            errorMessage += "Ingrese su correo institucional";
        }else if(!this.txtCorreo.getText().matches("^[a-z._]+@unprg\\.edu\\.pe$")){
            errorMessage += "Correo no pertenece a la UNPRG";
        }
        
        if(errorMessage.isEmpty()){
            return true;
        }else {
            MostrarMensaje.error(errorMessage);
            return false;
        }
    }
