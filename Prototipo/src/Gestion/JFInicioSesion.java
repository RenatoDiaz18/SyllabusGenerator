    private boolean validarCorreo() {
        boolean banCorreo = true;
        
        if(this.txtEmail.getText().isBlank() || this.txtEmail.getText().isEmpty() || this.txtEmail.getForeground() == Color.LIGHT_GRAY){
            errorCorreo ="Se requiere un correo para continuar.\n";
            this.lblErrorCorreo.setText(errorCorreo);
            this.lblErrorCorreo.setVisible(true);
            banCorreo = false;
        }
        
        if(!this.txtEmail.getText().matches("^[a-z._]+@unprg\\.edu\\.pe$")){
            errorCorreo += "Ingrese un correo válido";
            this.lblErrorCorreo.setText(errorCorreo);
            this.lblErrorCorreo.setVisible(true);
            banCorreo = false;
        }

        return banCorreo;
    }
    
    private boolean validarContraseña() {
        boolean banContraseña = true;
        
        if(String.valueOf(this.pwdContraseña.getPassword()).isBlank() || this.txtEmail.getText().isEmpty() || this.pwdContraseña.getForeground() == Color.LIGHT_GRAY){
            errorContraseña ="Se requiere una contraseña para continuar.\n";
            this.lblErrorContraseña.setText(errorContraseña);
            this.lblErrorContraseña.setVisible(true);
            banContraseña = false;
        }
        return banContraseña;
    }

    private void limpiarDatos() {
        this.txtEmail.setText("");
        this.pwdContraseña.setText("");
    }
