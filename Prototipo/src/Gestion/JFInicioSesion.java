    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        ocultarErrorMessages();
        boolean correoValidado = validarCorreo();
        boolean contraseñaValidada = validarContraseña();
        
        if(correoValidado && contraseñaValidada){
            boolean correoEncontrado = false, contraseñaEncontrada = false;
            ocultarErrorMessages();
            for(Docente doc : docentes){
                if(doc.getCorreo().equals(this.txtEmail.getText())){
                    correoEncontrado = true;
                    if(doc.getContraseña().equals(String.valueOf(this.pwdContraseña.getPassword()))){
                        contraseñaEncontrada = true;
                        
                        docenteLogged = doc;
                    }else {
                        errorContraseña = "Contraseña incorrecta.";
                        this.lblErrorContraseña.setText(errorContraseña);
                        this.lblErrorContraseña.setVisible(true);
                            
                        if(!doc.getCorreo().equals(intentoCorreo)){
                            intentoCorreo = doc.getCorreo();
                            contadorIntentos = 1;
                        } else {
                            contadorIntentos++;
                        }
                        
                        this.lblIntentos.setText("Intentos restantes: " + (3-contadorIntentos));
                        if(!this.lblIntentos.isVisible()){
                            this.lblIntentos.setVisible(true);
                        }
                    }
                    
                    break;
                }
            }
            
            if(!correoEncontrado){
                errorCorreo = "Usuario no encontrado";
                this.lblErrorCorreo.setText(errorCorreo);
                this.lblErrorCorreo.setVisible(true);
            }else if(contraseñaEncontrada){
                this.dispose();
                Prototipo.mostrarVentanaGenerador();
            }else if(contadorIntentos >= 3){
                
                JDPreguntaSeguridad frmPreguntaSeguridad = new JDPreguntaSeguridad(null, true);
                frmPreguntaSeguridad.setEnunciadoPregunta(intentoCorreo);
                frmPreguntaSeguridad.setLocationRelativeTo(this);
                ocultarErrorMessages();
                limpiarDatos();
                frmPreguntaSeguridad.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

