    private boolean ValidarDatos() {
        String errorMessage = "";
        
        if(this.txtNombres.getText().isBlank() || this.txtNombres.getText().isEmpty() || this.txtNombres.getText().equals("Ingrese su nombre")){
            errorMessage += "\n- Nombres.";
        }
        
        if(this.txtApellidos.getText().isBlank() || this.txtApellidos.getText().isEmpty() || this.txtApellidos.getText().equals("Ingrese sus apellidos")){
            errorMessage += "\n- Apellidos.";
        }
        
        if(this.cmbGenero.getSelectedIndex()==-1){
            errorMessage += "\n- Género.";
        }
        
        if(this.cmbFacultad.getSelectedIndex()==-1){
            errorMessage += "\n- Facultad.";
        }
        
        if(this.cmbEscuela.getSelectedIndex()==-1){
            errorMessage += "\n- Escuela.";
        }
        
        if(this.cmbGrado.getSelectedIndex()==-1){
            errorMessage += "\n- Grado académico.";
        }
        if(this.txtCorreo.getText().isBlank() || this.txtCorreo.getText().isEmpty() || this.txtCorreo.getText().equals("example@unprg.edu.pe")){
            errorMessage += "\n- Correo.";
        }
                
        if(String.valueOf(this.pwdContraseña.getPassword()).isBlank() || String.valueOf(this.pwdContraseña.getPassword()).isEmpty() || this.pwdContraseña.getForeground() == Color.LIGHT_GRAY){
            errorMessage += "\n- Contraseña.";
        }
        
        if(String.valueOf(this.pwdConfirmContraseña.getPassword()).isBlank() || String.valueOf(this.pwdConfirmContraseña.getPassword()).isEmpty() || this.pwdConfirmContraseña.getForeground() == Color.LIGHT_GRAY){
            errorMessage += "\n- Confirmación de contraseña.";
        }
        
        if(!errorMessage.isEmpty()){
            MostrarMensaje.error("Datos no registrados: " + errorMessage);
            return false;
        }
                
        String regexContraseña = "^(?=.*[A-Za-z])(?=.*\\d).{8,}$";
            
        if (!String.valueOf(this.pwdContraseña.getPassword()).matches(regexContraseña)) {
            errorMessage += "Se necesita una contraseña válida.\n- 8 caracteres como mínimo.\n- Al menos un número.\n- Al menos una letra.";
            MostrarMensaje.error(errorMessage);
            return false;
        }
        
        if(!String.valueOf(this.pwdContraseña.getPassword()).equals(String.valueOf(this.pwdConfirmContraseña.getPassword()))){
            errorMessage += "Las contraseñas no coinciden.";
            MostrarMensaje.error(errorMessage);
            return false;
        }
        
        String regexCorreo = "^[a-z._]+@unprg\\.edu\\.pe$";
        if(!this.txtCorreo.getText().matches(regexCorreo)){
            errorMessage += "Ingrese un correo válido";
            MostrarMensaje.error(errorMessage);
            return false;
        }
        if(this.cmbPregunta.getSelectedIndex() == -1){
            errorMessage += "Seleccione pregunta de seguridad en caso de olvidar su contraseña.";
            MostrarMensaje.error(errorMessage);
            return false;
        }
        
        if(this.txtRespuesta.getText().isBlank() || this.txtRespuesta.getText().isEmpty()){
            errorMessage += "Ingrese una respuesta a la pregunta de seguridad.";
            MostrarMensaje.error(errorMessage);
            return false;
        }
        
        return true;
    }

