    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        boolean banCorreo = false;
        
        if(ValidarDatos()){
            for(Docente doc : docentes){
                if(doc.getCorreo().equals(this.txtCorreo.getText())){
                    MostrarMensaje.error("Correo ya registrado.");
                    banCorreo = true;
                }
            }
            
            if(!banCorreo){
                //Docente
                //String nombres
                //String apellidos
                //String genero
                //Integer facultad
                //Integer escuela
                //String gradoAcademico
                //String correo
                //String contraseña
                //int preguntaSeguridad
                //String respuestaSeguridad
                Docente doc = new Docente(
                                this.txtNombres.getText(),
                                this.txtApellidos.getText(),
                                (String) this.cmbGenero.getSelectedItem(),
                                this.cmbFacultad.getSelectedIndex(),
                                this.cmbEscuela.getSelectedIndex(),
                                (String) this.cmbGrado.getSelectedItem(),
                                this.txtCorreo.getText(),
                                String.valueOf(this.pwdContraseña.getPassword()),
                                this.cmbPregunta.getSelectedIndex(),
                                this.txtRespuesta.getText()
                        );
                        
                docentes.add(doc);
                actualizarDataDocentes(doc);

                MostrarMensaje.confirm("Usuario registrado");
                this.dispose();
                Prototipo.login();
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

